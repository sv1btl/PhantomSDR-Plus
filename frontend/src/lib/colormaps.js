// https://github.com/csete/gqrx/blob/master/src/qtgui/plotter.cpp
const gqrxData = []
for (let i = 0; i < 256; i++) {
  if (i < 20) {
    // level 0: black background
    gqrxData.push([0, 0, 0])
  } else if ((i >= 20) && (i < 70)) {
    // level 1: black -> blue
    gqrxData.push([0, 0, 140 * (i - 20) / 50])
  } else if ((i >= 70) && (i < 100)) {
    // level 2: blue -> light-blue / greenish
    gqrxData.push([60 * (i - 70) / 30, 125 * (i - 70) / 30, 115 * (i - 70) / 60 + 140])
  } else if ((i >= 100) && (i < 150)) {
    // level 3: light blue -> yellow
    gqrxData.push([195 * (i - 100) / 50 + 60, 130 * (i - 100) / 50 + 125, 255 - (255 * (i - 100) / 50)])
  } else if ((i >= 150) && (i < 250)) {
    // level 4: yellow -> red
    gqrxData.push([255, 255 - 255 * (i - 150) / 100, 0])
  } else if (i >= 250) {
    // level 5: red -> white
    gqrxData.push([255, 255 * (i - 250) / 5, 255 * (i - 250) / 5])
  }
  gqrxData[i] = gqrxData[i].map(x => x / 255)
}

// Replica of WebSDR Colormap to replicate the cool look of these colors!
const twenteData = [];
for (let i = 0; i < 256; i++) {
  if (i < 105) {
    // level 0: black to dark blue (#0d0086)
    const r = 13 * i / 105;
    const g = 0;
    const b = 134 * i / 105;
    twenteData.push([r, g, b]);
  }  else if (i < 192) {
    // level 2: dark purple (#6200b0) to yellow (#fffec3)
    const r = 98 + (255 - 98) * (i - 96) / 63;
    const g = 0 + (254 - 0) * (i - 96) / 63;
    const b = 176 + (195 - 176) * (i - 96) / 63;
    twenteData.push([r, g, b]);
  } else {
    // level 3: yellow (#fffec3)
    const r = 255;
    const g = 254;
    const b = 195;
    twenteData.push([r, g, b]);
  }
  // Normalize RGB values to the range 0-1
  twenteData[i] = twenteData[i].map(x => x / 255);
}

// SpectraVU Colormap
const spectraVUData = [];

for (let i = 0; i < 256; i++) {
  if (i < 64) {
    // Dark blue to blue
    const r = 0;
    const g = 0;
    const b = 128 + (i * 2);
    spectraVUData.push([r, g, b]);
  } else if (i < 128) {
    // Blue to green
    const r = 0;
    const g = (i - 64) * 4;
    const b = 255 - ((i - 64) * 4);
    spectraVUData.push([r, g, b]);
  } else if (i < 192) {
    // Green to yellow
    const r = (i - 128) * 4;
    const g = 255;
    const b = 0;
    spectraVUData.push([r, g, b]);
  } else {
    // Yellow to red
    const r = 255;
    const g = 255 - ((i - 192) * 4);
    const b = 0;
    spectraVUData.push([r, g, b]);
  }
  // Normalize RGB values to the range 0-1
  spectraVUData[i] = spectraVUData[i].map(x => Math.min(x, 255) / 255);
}

// Custom Colormap
const customData = [];

for (let i = 0; i < 256; i++) {
  let r, g, b;

  if (i < 60) {
    // Dark blue to blue (slightly shorter)
    r = 0;
    g = 0;
    b = 40 + i * 1; //was 50
  } else if (i < 100) {
    // Blue to green
    r = 0;
    g = (i - 60) * 10;
    b = 255 - ((i - 255) * 16);
  } else if (i < 150) {
    // Green to yellow (slightly longer for smoother yellow)
    r = (i - 48) * 20;
    g = 255;
    b = 0;
  } else if (i < 200) {
    // Extended yellow to red (more yellow/orange range)
    r = 255;
    g = 255 - ((i - 32) * 32); // slower fade from yellow to orange
    b = 0;
  } else {
    // Extended deep red range
    r = 255;
    g = Math.max(0, 15 - (i - 16) * 32); // deep red tones
    b = 0;
  }
  // Normalize RGB values to 0–1
  customData.push([
    Math.min(r, 255) / 255,
    Math.min(g, 255) / 255,
    Math.min(b, 255) / 255,
  ]);
}


